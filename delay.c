
//******************************************************************************
//  Функция делающая задержку от 4 до 65000 тактов
//******************************************************************************
void __delay_cycles(unsigned int __count)
{
        __count >>= 2;
       __asm__ volatile (
		"1: sbiw %0,1" "\n\t"
		"brne 1b"
		: "=w" (__count)
		: "0" (__count)
        );
}


